#if UNITY_EDITOR

    private const int MAX_LINE_LENGTH = 26; // 한 줄 최대 한국어 글자 수 기준

    /// <summary>
    /// 모든 챕터 라인의 세그먼트(\\n 분리 후) 중 MAX_LINE_LENGTH 초과 항목을 LogWarning.
    /// 유니티 에디터 초기화 시 [InitializeOnLoadMethod]로 자동 실행하거나
    /// 메뉴에서 수동 호출 가능.
    ///
    /// 호출:
    ///   StoryDatabase.ValidateAllLines();
    /// </summary>
    [UnityEditor.InitializeOnLoadMethod]
    public static void ValidateAllLines()
    {
        var allLists = new System.Collections.Generic.List<(string key, System.Collections.Generic.List<CutsceneLine> lines)>
        {
            ("Chapter1",          GetChapter1Lines()),
            ("Chapter2",          GetChapter2Lines()),
            ("Chapter3_Decision", GetChapter3_DecisionLines()),
            ("Chapter4_Pre",      GetChapter4_PreBossLines()),
            ("Chapter4_Post",     GetChapter4_PostBossLines()),
            ("Chapter5",          GetChapter5Lines()),
            ("Chapter6_Pre",      GetChapter6_PreBossLines()),
            ("Chapter6_Post",     GetChapter6_PostBossLines()),
            ("Chapter7_Badge",    GetChapter7_BadgeLines()),
            ("Chapter8_Heli",     GetChapter8_HelicopterLines()),
            ("Chapter8_Post",     GetChapter8_PostFightLines()),
            ("Chapter9",          GetChapter9_MechanicLines()),
            ("Chapter10_Pre",     GetChapter10_PreBossLines()),
            ("Chapter10_Reveal",  GetChapter10_RevealLines()),
            ("Chapter11_Collapse",GetChapter11_CollapseLines()),
            ("Chapter11_Accept",  GetChapter11_AcceptanceLines()),
            ("Chapter11_Awaken",  GetChapter11_AwakeningLines()),
            ("Chapter12_Open",    GetChapter12_OpeningLines()),
        };

        bool hasError = false;
        foreach (var (key, list) in allLists)
        {
            for (int i = 0; i < list.Count; i++)
            {
                var line = list[i];
                if (string.IsNullOrEmpty(line.text)) continue;

                foreach (var segment in line.text.Split('\n'))
                {
                    // 공백 제거 후 실제 글자 수 계산 (한국어 1글자 = 1카운트)
                    int len = segment.Replace(" ", "").Length;
                    if (len > MAX_LINE_LENGTH)
                    {
                        Debug.LogWarning(
                            $"[StoryDatabase] {key}[{i}] 세그먼트 {len}자 초과 " +
                            $"(최대 {MAX_LINE_LENGTH}): \"{segment}\"");
                        hasError = true;
                    }
                }
            }
        }

        if (!hasError)
            Debug.LogWarning("[StoryDatabase] 텍스트 길이 전수 검증 완료 — 이상 없음.");
    }

#endif
